package codelab;

public class converter  {
     
    protected double setValuetoM(double valuem){
        return valuem*0.9144;
    }
    
    protected double setValuetoCM(double valuecm){
        return valuecm*91.44; 
    }
    
    protected double setValuetoFeet(double valuef){
        return valuef*3;
    }
    
    protected double setValuetoMiles(double valuemi){
        return valuemi*0.000568182;
    }
}
